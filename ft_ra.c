void	ft_ra(s_stack **a)
{
	s_stack	*i;

	if (!a || !*a || !(*a)->next)
		return ;
	i = *a;
	while (i->next != '\0')
		i = i->next;
	i->next = *a;
	*a = (*a)->next;
	i->next->next = NULL;
}
