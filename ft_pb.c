#include "push_swap.h"

void	ft_pb(s_stack **a, s_stack **b)
{
	s_stack	*temp;

	if (!a || !*a)
		return ;
	temp = *a;
	*a = (*a)->next;
	temp->next = *b;
	*b = temp;
	write(1, "pb\n", 3);
}
