#include "push_swap.h"

void	ft_pa(s_stack **a, s_stack **b)
{
	s_stack	*temp;

	if (!b || !*b)
		return ;
	temp = *b;
	*b = (*b)->next;
	temp->next = *a;
	*a = temp;
	write(1, "pa\n", 3);
}
