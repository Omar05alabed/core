int	is_number(char *str)
{
	int	i;

	i = 0;
	if (!str[i])
		return (0);
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (str[i] == '\0')
		print_error();
	while (str[i])
	{
		if (str[i] < 0 || str[i] > 9)
			print_error();
		i++;
	}
	return (1);
}
